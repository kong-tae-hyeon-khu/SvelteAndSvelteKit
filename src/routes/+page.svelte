<h3>안녕하세요. 웹 어플리케이션 개발에 관심을 갖고 있는 공태현입니다.</h3>
이 웹 페이지는 SvelteKit 과 Spring Boot 로 API 와 Page 를 구성하고, EC2에 설치한 Nginx 를 통해 배포하고 있습니다.
<p><strong>Home</strong> : 메인 페이지 (현재 보고 계신 페이지입니다.)</p>
<p><strong>Posts</strong> : 게시글 목록을 볼 수 있는 페이지입니다.</p>
<p><strong>Write</strong> : 글을 쓰기 위한 페이지입니다.</p>

현재는 저의 계정으로 로그인한 상황에서만 글을 작성할 수 있습니다. (JWT 를 통한 권한 확인)<br>
현재 페이지를 수정하고 페이지 호스팅은 github.io 를 통해서 하고 서버는 EC2 를 통해 배포하여 연결할 에정입니다.


